#include "libft.h"

int						nmatch(char *s1, char *s2)
{
	if (*s2 == '*')
	{
		return (!*s1 ? nmatch(s1, s2 + 1) :
			nmatch(s1, s2 + 1) + nmatch(s1 + 1, s2));
	}
	if (!*s1)
	{
		return (*s2 ? 0 : 1);
	}
	return (*s1 == *s2 ? nmatch(s1 + 1, s2 + 1) : 0);
}
